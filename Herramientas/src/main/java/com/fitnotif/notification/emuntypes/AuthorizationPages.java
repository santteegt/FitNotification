package com.fitnotif.notification.emuntypes;

/**
 * Enum de paginas con informacion a ser mostradas en el dispositivo movil
 * @author santiago
 */
public enum AuthorizationPages {
    
    CONSOLIDATED("E-GENERAL"),    
    MOVEMENT("SALDOS"),
    DOCUMENT("DOCUMENTOS"),
    USERS("USUARIOS"),
    OFFICERS("OFICIALES"),
    ACCOUNTS("CUENTAS"),
    WARRANTY("GARANTIA");
    
    private String page;
    
    private AuthorizationPages(String page){
        this.page=page;
    }
    
    public String getPage(){
        return this.page;
    }
    
}
